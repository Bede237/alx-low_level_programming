#include "3-calc.h"
/**
 *op_add - returns the addition of two numbers
 *@a: parameter
 *@b: parameter
 *
 *return: (a+b)
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - returns subtrsction of two nums
 *@a: parameter
 *@b: parameter
 *
 * return: (a-b)
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - returns multiplication of two nums
 *@a: parameter
 *@b: parameter
 *
 *return: (a*b)
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - returns the devision between a and b
 *@a: parameter
 *@b: parameter
 *
 * return: (a/b)
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - returns the modulus of a and b
 *@a: parameter
 *@b: parameter
 *
 *return: (a % b)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
